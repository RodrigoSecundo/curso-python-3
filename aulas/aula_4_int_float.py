# Tipos int e float
# int -> números inteiros, sem parte decimal
# float -> números com parte decimal

# O tipo int representa qualquer número positivo ou negativo sem parte decimal.
# int sem sinal é considerado positivo.

print(10)        # int positivo
print(-5)        # int negativo
print(0)         # int zero
print(type(10))  # type verifica o tipo do valor, se colocado em print exibe o tipo na tela

# O tipo float representa números com parte decimal, positivos ou negativos.
# Mais uma vez, float sem sinal é considerado positivo.

print(3.14)         # float positivo
print(-2.71)        # float negativo
print(0.0)          # float zero
print(type(3.14))   # novamente: type verifica o tipo do valor, se colocado em print exibe o tipo na tela